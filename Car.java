public class Car
{
 private String make;
 private String model;
 private int price;
 
 
 
 public String GetMake()
 {
  return make;
 }
 public String GetModel()
 {
  return model;
 }
 public int GetPrice()
 {
  return price;
 } 
 public void SetMake(String name)
 {
  make=name;
 }
 public void SetModel(String type)
 {
  model=type;
 }
 public void SetPrice(int cost)
 {
  price=cost;
 }
}


